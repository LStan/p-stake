use pinocchio::{
    account_info::AccountInfo, program_error::ProgramError, pubkey::Pubkey, sysvars::clock,
};
use pinocchio_pubkey::pubkey;

const VOTE_STATE_ID: Pubkey = pubkey!("Vote111111111111111111111111111111111111111");
pub const MINIMUM_DELINQUENT_EPOCHS_FOR_DEACTIVATION: u64 = 5;

pub fn acceptable_reference_epoch_credits(
    vote_account_info: &AccountInfo,
    current_epoch: clock::Epoch,
) -> Result<bool, ProgramError> {
    if !vote_account_info.is_owned_by(&VOTE_STATE_ID) {
        return Err(ProgramError::IncorrectProgramId);
    }

    let data = vote_account_info.try_borrow_data()?;

    let mut offset = skip_to_epoch_credits(&data)?;
    let mut epoch_credits_count = u64::from_le_bytes(data[offset..offset + 8].try_into().unwrap());

    if let Some(epoch_index) =
        epoch_credits_count.checked_sub(MINIMUM_DELINQUENT_EPOCHS_FOR_DEACTIVATION)
    {
        let mut epoch = current_epoch;

        offset += 8;

        while epoch_index != epoch_credits_count {
            epoch_credits_count -= 1;
            let vote_epoch = u64::from_le_bytes(
                data[offset + epoch_credits_count as usize * (8 + 8 + 8)
                    ..offset + epoch_credits_count as usize * (8 + 8 + 8) + 8]
                    .try_into()
                    .unwrap(),
            );
            if vote_epoch != epoch {
                return Ok(false);
            }
            epoch = epoch.saturating_sub(1);
        }
        Ok(true)
    } else {
        Ok(false)
    }
}

pub fn get_last_epoch(
    vote_account_info: &AccountInfo,
) -> Result<Option<clock::Epoch>, ProgramError> {
    if !vote_account_info.is_owned_by(&VOTE_STATE_ID) {
        return Err(ProgramError::IncorrectProgramId);
    }

    let data = &vote_account_info.try_borrow_data()?;

    get_last_epoch_inner(data)
}

pub fn get_credits(vote_account_info: &AccountInfo) -> Result<u64, ProgramError> {
    if !vote_account_info.is_owned_by(&VOTE_STATE_ID) {
        return Err(ProgramError::IncorrectProgramId);
    }

    let data = &vote_account_info.try_borrow_data()?;

    get_credits_inner(data)
}

fn get_last_epoch_inner(data: &[u8]) -> Result<Option<u64>, ProgramError> {
    let mut offset = skip_to_epoch_credits(data)?;
    let epoch_credits_count = u64::from_le_bytes(data[offset..offset + 8].try_into().unwrap());

    if epoch_credits_count == 0 {
        Ok(None)
    } else {
        offset += 8 + (epoch_credits_count as usize - 1) * (8 + 8 + 8);
        Ok(Some(u64::from_le_bytes(
            data[offset..offset + 8].try_into().unwrap(),
        )))
    }
}

fn get_credits_inner(data: &[u8]) -> Result<u64, ProgramError> {
    let mut offset = skip_to_epoch_credits(data)?;
    let epoch_credits_count = u64::from_le_bytes(data[offset..offset + 8].try_into().unwrap());

    if epoch_credits_count == 0 {
        Ok(0)
    } else {
        offset += 8 + (epoch_credits_count as usize - 1) * (8 + 8 + 8) + 8;
        Ok(u64::from_le_bytes(
            data[offset..offset + 8].try_into().unwrap(),
        ))
    }
}

// TODO: this function adds a lot of size to the program, optimize
// returns the number of bytes to the start of the epoch credits. Also checks the length of the data
fn skip_to_epoch_credits(data: &[u8]) -> Result<usize, ProgramError> {
    // variant, node_pubkey, authorized_withdrawer, commission, votes count
    if data.len() < 4 + 32 + 32 + 1 + 8 {
        return Err(ProgramError::InvalidAccountData);
    }
    // has latency or not
    let latency_size = match data[0] {
        // V1_14_11. substantially different layout and data from V0_23_5
        1 => 0,
        // Current. the only difference from V1_14_11 is the addition of a slot-latency to each vote
        2 => 1,
        _ => return Err(ProgramError::InvalidAccountData),
    };

    let vote_count = u64::from_le_bytes(
        data[4 + 32 + 32 + 1..4 + 32 + 32 + 1 + 8]
            .try_into()
            .unwrap(),
    );

    let mut current = 4 + 32 + 32 + 1 + 8; // variant, node_pubkey, authorized_withdrawer, commission, votes count
    current += vote_count * (latency_size + 8 + 4); // latency, slot, confirmation_count

    if data.len() < current as usize + 1 + 8 + 8 {
        return Err(ProgramError::InvalidAccountData);
    }

    // root_slot
    match data[current as usize] {
        0 => {
            current += 1;
        }
        1 => {
            current += 1 + 8;
        }
        _ => {
            return Err(ProgramError::InvalidAccountData);
        }
    }

    let authorized_voter_count = u64::from_le_bytes(
        data[current as usize..current as usize + 8]
            .try_into()
            .unwrap(),
    );
    current += 8 + authorized_voter_count * (8 + 32); // epoch, authorized_voter
    current += 32 * (32 + 8 + 8) + 8 + 1; // prior_voters

    if data.len() < current as usize + 8 {
        return Err(ProgramError::InvalidAccountData);
    }

    let epoch_credits_count = u64::from_le_bytes(
        data[current as usize..current as usize + 8]
            .try_into()
            .unwrap(),
    );

    // epoch_credits, last_timestamp
    if data.len() < current as usize + 8 + (epoch_credits_count as usize) * (8 + 8 + 8) + (8 + 8) {
        return Err(ProgramError::InvalidAccountData);
    }

    Ok(current as usize)
}

